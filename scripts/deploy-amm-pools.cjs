const fs = require("fs");
const path = require("path");
const { ethers } = require("hardhat");
const STATE_PATH = path.join(__dirname, "..", "cache", "amm-pools-state.json");

function txOverridesFromFeeData(feeData) {
  const txOverrides = {};
  if (feeData.maxFeePerGas && feeData.maxPriorityFeePerGas) {
    txOverrides.maxPriorityFeePerGas = feeData.maxPriorityFeePerGas + 2_000_000_000n;
    txOverrides.maxFeePerGas = feeData.maxFeePerGas + 5_000_000_000n;
  } else if (feeData.gasPrice) {
    txOverrides.gasPrice = feeData.gasPrice + 2_000_000_000n;
  }
  return txOverrides;
}

function writePoolsTs(pairs, outPath) {
  const lines = [];
  lines.push('import { type Address } from "viem";');
  lines.push("");
  lines.push("// Auto-generated by scripts/deploy-amm-pools.cjs");
  lines.push('export type AmmPairKey = `${string}/${string}`;');
  lines.push("");
  lines.push("export const HUB_AMM_POOLS: Record<AmmPairKey, Address> = {");
  for (const { key, address } of pairs) {
    lines.push(`  "${key}": "${address}" as Address,`);
  }
  lines.push("} as const;");
  lines.push("");
  lines.push("export function ammPairKey(a: string, b: string): AmmPairKey {");
  lines.push("  return `${String(a).toUpperCase()}/${String(b).toUpperCase()}` as AmmPairKey;");
  lines.push("}");
  lines.push("");

  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, lines.join("\n"), "utf8");
}

function readState() {
  if (!fs.existsSync(STATE_PATH)) return { pairs: [] };
  try {
    return JSON.parse(fs.readFileSync(STATE_PATH, "utf8"));
  } catch {
    return { pairs: [] };
  }
}

function writeState(state) {
  fs.mkdirSync(path.dirname(STATE_PATH), { recursive: true });
  fs.writeFileSync(STATE_PATH, JSON.stringify(state, null, 2), "utf8");
}

function hasPair(state, key) {
  return state.pairs.some((p) => p.key === key);
}

function addPair(state, key, address) {
  if (!hasPair(state, key)) state.pairs.push({ key, address });
  writeState(state);
}

async function deployPool(token0, token1, txOverrides) {
  const Pool = await ethers.getContractFactory("DeFAIAMMPool");
  let attempt = 0;
  let lastErr;
  while (attempt < 5) {
    try {
      const bump = BigInt(attempt) * 2_000_000_000n;
      const overrides = { ...txOverrides };
      if (overrides.maxPriorityFeePerGas) overrides.maxPriorityFeePerGas += bump;
      if (overrides.maxFeePerGas) overrides.maxFeePerGas += bump * 2n;
      if (overrides.gasPrice) overrides.gasPrice += bump;
      const pool = await Pool.deploy(token0, token1, overrides);
      await pool.waitForDeployment();
      return pool;
    } catch (err) {
      lastErr = err;
      attempt += 1;
      if (!String(err?.message || err).toLowerCase().includes("priority is too low")) {
        break;
      }
    }
  }
  throw lastErr;
}

async function seedPool(poolAddr, token0, token1, amount0, amount1, deployer, txOverrides) {
  const pool = await ethers.getContractAt("DeFAIAMMPool", poolAddr);
  const t0 = await ethers.getContractAt("MockERC20Permit", token0);
  const t1 = await ethers.getContractAt("MockERC20Permit", token1);

  const ensureBalance = async (token, needed, symbol) => {
    const bal = await token.balanceOf(deployer.address);
    if (bal >= needed) return;
    const shortfall = needed - bal;
    try {
      await (await token.mint(deployer.address, shortfall, txOverrides)).wait();
    } catch {
      const balAfter = await token.balanceOf(deployer.address);
      if (balAfter < needed) {
        throw new Error(`Cannot seed pool: insufficient ${symbol} balance and token is not mintable.`);
      }
    }
  };

  await ensureBalance(t0, amount0, token0);
  await ensureBalance(t1, amount1, token1);
  await (await t0.approve(poolAddr, amount0, txOverrides)).wait();
  await (await t1.approve(poolAddr, amount1, txOverrides)).wait();
  await (await pool.addLiquidity(amount0, amount1, 0, 0, deployer.address, txOverrides)).wait();
}

